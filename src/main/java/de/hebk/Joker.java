package de.hebk;

public class Joker {
    private boolean revive;
    private boolean audience;
    private boolean fifty_fifty;
    private boolean call;

    public boolean isRevive() {
        return revive;
    }

    public void setRevive(boolean revive) {
        this.revive = revive;
    }

    public boolean isAudience() {
        return audience;
    }

    public void setAudience(boolean audience) {
        this.audience = audience;
    }

    public boolean isFifty_fifty() {
        return fifty_fifty;
    }

    public void setFifty_fifty(boolean fifty_fifty) {
        this.fifty_fifty = fifty_fifty;
    }

    public boolean isCall() {
        return call;
    }

    public void setCall(boolean call) {
        this.call = call;
    }
}
