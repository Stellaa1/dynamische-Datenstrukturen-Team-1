package de.hebk;

public class Cheats {
    private boolean infJoker;
    private boolean infTime;
    private boolean infLife;
    private boolean alwaysFiftyFifty;
    private boolean alwaysCall;

    public boolean isInfJoker() {
        return infJoker;
    }

    public void setInfJoker(boolean infJoker) {
        this.infJoker = infJoker;
    }

    public boolean isInfTime() {
        return infTime;
    }

    public void setInfTime(boolean infTime) {
        this.infTime = infTime;
    }

    public boolean isInfLife() {
        return infLife;
    }

    public void setInfLife(boolean infLife) {
        this.infLife = infLife;
    }

    public boolean isAlwaysFiftyFifty() {
        return alwaysFiftyFifty;
    }

    public void setAlwaysFiftyFifty(boolean alwaysFiftyFifty) {
        this.alwaysFiftyFifty = alwaysFiftyFifty;
    }

    public boolean isAlwaysCall() {
        return alwaysCall;
    }

    public void setAlwaysCall(boolean alwaysCall) {
        this.alwaysCall = alwaysCall;
    }
}
